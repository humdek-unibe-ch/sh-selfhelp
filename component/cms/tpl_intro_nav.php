        <h1>This is a Navigation Page</h1>
        <p>
            Like a normal page, a <i>navigation page</i> consist of a set of sections.
            However, unlike a normal page, a navigation pages does not render sections one below the other.
        </p>
        <p>
            A navigation page renders only one root section (or navigation section) at a time. The purpose of a navigation page is to navigate from one section to another without changing the base url.
            The section to render is indicated by a trailing id in the url.
        </p>
        <p>
        A navigation page should include some sort of navigation pannel that allows to select a specific section to render. For example, this CMS uses a <code>nestedList</code> style to navigate between the different sections.
        </p>
        <p>
            The card <code>Navigation Hierarchy</code> on the left shows how the navigation sections are ordered. Note that this list can be hierarchical which allows to create navigation section children.
            The order and the hierarchy of the naviagtion sections can be changed in each individual navigation section with the field <code>navigation</code>.
            Only navigation sections have this field.
        </p>
    </div>
</div>
